MARY DOWEY (www.marydowey.com) has been a wine, food and travel writer contributing to 
well-known publications in the UK, the US and her native Ireland for over 20 years. 
Dividing her time between Dublin and Uzès, she has come to know much of Provence by 
researching ten wine and food articles about the region for the leading wine magazine 
Decanter. She has also researched and written a section on the Southern Rhône for a 
major book, Opus Vino, to be published in late 2010. She is passionate about Provence 
and its produce and has built up a fantastic network of useful contacts: most wine 
producers are also food lovers!
